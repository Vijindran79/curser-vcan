/**
 * Get logo URL for a carrier/platform name
 * Automatically handles case variations and provides fallback
 */
export function getLogoUrl(carrierName: string): string {
  const normalizedName = carrierName.toUpperCase().trim();
  
  // Direct match
  if (logoMap[normalizedName]) {
    return logoMap[normalizedName];
  }
  
  // Try variations (remove spaces, hyphens, special chars)
  const variations = [
    normalizedName.replace(/\s+/g, ''),
    normalizedName.replace(/[-_]/g, ''),
    normalizedName.replace(/\s+[-_]\s+/g, ' '),
    normalizedName.split(' ')[0], // First word
    normalizedName.split(' ').slice(0, 2).join(' '), // First two words
  ];
  
  for (const variation of variations) {
    if (logoMap[variation]) {
      return logoMap[variation];
    }
  }
  
  // Check for partial matches (if carrier name contains known brand)
  for (const [key, url] of Object.entries(logoMap)) {
    if (key.length > 3 && (normalizedName.includes(key) || key.includes(normalizedName))) {
      return url;
    }
  }
  
  return logoMap.DEFAULT;
}

/**
 * Check if a carrier/platform has a logo
 */
export function hasLogo(carrierName: string): boolean {
    const normalizedName = carrierName.toUpperCase().trim();
  
    // Direct match
    if (logoMap[normalizedName]) {
        return true;
    }
  
    // Try variations
    const variations = [
        normalizedName.replace(/\s+/g, ''),
        normalizedName.replace(/[-_]/g, ''),
        normalizedName.replace(/\s+[-_]\s+/g, ' '),
        normalizedName.split(' ')[0],
        normalizedName.split(' ').slice(0, 2).join(' '),
    ];
  
    for (const variation of variations) {
        if (logoMap[variation]) {
            return true;
        }
    }
  
    // Check for partial matches
    for (const key of Object.keys(logoMap)) {
        if (key.length > 3 && (normalizedName.includes(key) || key.includes(normalizedName))) {
            return true;
        }
    }
  
    return false;
}